package pl.alx.kpij.Ogloszenia;

public class Motorcycle extends Car{
	
	public Motorcycle (String type, String title, String place, double price, String phoneNumber, String mark, String model, String fuel, int year, int enginePower, double mileage, double engineCapacity) {
		setType(type);
		setTitle(title);
		setPlace(place);
		setPrice(price);
		setPhoneNumber(phoneNumber);
		setMark(mark);
		setModel(model);
		setFuel(fuel);
		setYear(year);
		setEnginePower(enginePower);
		setMileage(mileage);
		setEngineCapacity(engineCapacity);
		
	}

}
